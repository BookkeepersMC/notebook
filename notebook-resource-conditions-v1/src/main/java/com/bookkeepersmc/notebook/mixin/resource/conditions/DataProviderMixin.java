package com.bookkeepersmc.notebook.mixin.resource.conditions;

import it.unimi.dsi.fastutil.objects.Object2IntOpenHashMap;
import org.spongepowered.asm.mixin.Dynamic;
import org.spongepowered.asm.mixin.Mixin;
import org.spongepowered.asm.mixin.injection.At;
import org.spongepowered.asm.mixin.injection.Inject;
import org.spongepowered.asm.mixin.injection.callback.CallbackInfo;

import net.minecraft.data.DataProvider;

import com.bookkeepersmc.notebook.api.resource.conditions.v1.ResourceConditions;

@Mixin(DataProvider.class)
public interface DataProviderMixin {
	@Dynamic("lambda method passed to Util.make")
	@Inject(method = "method_43808", at = @At("HEAD"))
	private static void notebook_injectResourceConditionsSortOrder(Object2IntOpenHashMap<String> map, CallbackInfo info) {
		map.put(ResourceConditions.CONDITIONS_KEY, -100);
	}
}
